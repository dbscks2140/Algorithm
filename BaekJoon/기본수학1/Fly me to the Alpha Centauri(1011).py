for _ in range(int(input())):
    x, y = map(int,input().split())
    t = 0
    i = 0
    while True:
        i += 1
        t += 2*i
        if(y - x <= t):
            if((t-i+1) > (y - x)):
                print(2*i-1)
                break
            else:
                print(2*i)
                break


